# range() generates the integer numbers
# between a given start and stop value

for i in range(3):
    print(f"Now we're in loop number: {i}")

print("-------")

for i in range(0, 3):
    print(f"Now we're in loop number: {i}")

print("-------")

for i in range(2, 5):
    print(f"Now we're in loop number: {i}")

    print("-------")

for i in range(0, 9, 3):
    print(f"Now we're in loop number: {i}")

